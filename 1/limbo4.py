from math import sqrt


def main(x, y, z):
    return (
        sqrt(
            ((82 * z**3 + 21 * y**2)**4 + (x - 23 * z**3 - y**2 / 6)**3) /
            (59 * y**2 + 85 * z**3 + x / 98)**2
        ) + (
            (12 * (53 * y + 9 * z**3 + x**2)**7 + 40 * x**3) /
            (86 * y**2 - (z**2 + 56 + 71 * x**3)**6 / 16)
        )
    )


print(main(0.09, -0.3, -0.21))
